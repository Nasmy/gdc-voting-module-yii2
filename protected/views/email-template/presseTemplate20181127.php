<h3 style="text-align: center;color:#595959">Communiqué de Presse</h3>
<h4 style="text-align: center;color:#595959;margin-top: -5px !important;">Paris, le 12 Février 2018</h4>
<div style="padding-left: 10%;padding-right: 10%;padding-top: 3%;padding-bottom: 10%">
    <h3 style="color:#d1bd23;font-size: 19px"><b>Les Globes de Cristal 2018</b></h3>
    <p style="color:#595959;">Sous la présidente d’honneur de Mademoiselle Isabelle Huppert</p>
    <p style="color:#595959;">La 12 éme Cérémonie des Globes de Cristal<br/>
        S’est déroulée, ce lundi 12 Février 2018 au Lido de Paris.
    </p>
    <p style="color:#595959;">Cette année, TV5 Monde, partenaire officiel et diffuseur,<br/>
        Retransmettra dans son intégralité cette 12 éme Edition et fera vivre les coulisses de cet<br/> évènement le
        jeudi 15 Février à 21 heures.
    </p>
    <p><b>La Cérémonie a récompensé le meilleur de la scène artistique, de la Culture Française mais<br/> aussi
            internationale.</b></p>
    <p><b>Les lauréats sont :</b></p>
    <p><b>Meilleur Film</b><br/>
    <li><b>120 Battements par minute</b> de Robin Campillo</li>
    </p>
    <p><b>Meilleure Actrice</b><br/>
    <li><b>Karin Viard</b> dans « Jalouse » de Stéphane et David Foekinos</li>
    </p>
    <p><b>Meilleur Acteur</b><br/>
    <li><b>Nahuel Pérez Biscayart</b> dans « 120 battements par minute » de Robin Campillo</li>
    </p>
    <p><b>Meilleure Pièce de théâtre</b><br/>
    <li><b>12 hommes en colère</b> d’après l’œuvre originale de Reginald Rose, adaptation de Francis Lombrail, mis en
        scène par Charles Tordjman (Théâtre Hébertot)
    </li>
    </p>
    <p><b>Meilleure Comédienne</b><br/>
    <li><b>Lina El Arabi </b> dans « Mon Ange » d’après « Angel » de Henry Naylor mis en scène par Jérémie Lippmann
        (Théâtre Tristan Bernard)
    </li>
    </p>
    <p><b>Meilleur Comédien</b><br/>
    <li><b>Michel Bouquet</b> dans « Le Tartuffe » de Molière, mis en scène par Michel Fau (Théâtre de la
        Porte-Saint-Martin)
    </li>
    </p>
    <p><b>Meilleur One Man Show</b><br/>
    <li><b>Vincent Dedienne</b> dans « S’il se passe quelque chose », mis en scène par Juliette Chaigneau et François
        Rollin (l’Olympia)
    </li>
    </p>
    <p><b>Meilleure Comédie Musicale</b><br/>
    <li><b>« Priscilla, folle du désert »</b> produit par Cheyenne Productions, mis en scène par Philippe Hersen (Le
        Casino de Paris)
    </li>
    </p>
    <p><b>Meilleur Roman / Essai</b><br/>
    <li><b>La tresse</b> de Laetitia Colombani (Éditions Grasset)</li>
    </p>
    <p><b>Meilleure Interprète féminine</b><br/>
    <li><b>Juliette Armanet</b> « Petite amie » (Barclay)</li>
    </p>
    <p><b>Meilleur Interprète masculin</b><br/>
    <li><b>Benjamin Biolay</b> « Volver » (Barclay)</li>
    </p>
    <p><b>Meilleur Série télévisée / Téléfilm</b><br/>
    <li><b>Dix pour cent</b> Saison 2, créée par Fanny Herrero (France 2)</li>
    </p>
    <p><b>Meilleur Film étranger</b><br/>
    <li><b>La La Land</b> de Damien Chazelle, États-Unis</li>
    </p>
    <p><b>Meilleure Série télévisée étrangère</b><br/>
    <li><b>The Handmaid’s Tale </b> Saison 1, créée par Bruce Miller, États-Unis (OCS)</li>
    </p>
